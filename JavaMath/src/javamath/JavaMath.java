package javamath;

public class JavaMath {

    public static void main(String[] args) {
        // PI
        System.out.println(Math.PI);
        System.out.println(Math.sin(30));
        System.out.println(Math.exp(90));
        System.out.println(Math.pow(2, 3));
        System.out.println(Math.sqrt(9));
        System.out.println(Math.max(60,550));
        System.out.println(Math.min(560,450));
        System.out.println(Math.floor(54.65));
        System.out.println(Math.ceil(54.65));
        System.out.println(Math.round(54.65));
        System.out.println(String.format("%.0f", Math.floor(Math.random()*10000)));
    }
    
}
