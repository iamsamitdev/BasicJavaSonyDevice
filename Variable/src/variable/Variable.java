package variable;

public class Variable {

    public static void main(String[] args) {
        // สร้างตัวแปร
        int anInt = 5;
        byte aByte = 15;
        long l = 2000l;
        short aShort = 129;
        char c = 'c';
        float f = 10.05f;
        double d = 30.75d;
        boolean oasis = true;
        String name = "\t\"Samit Koyom\"\n\n\tI am programmer \u2764";
        
        System.out.println(anInt+12);
        System.out.println(aByte-8);
        System.out.println(l*3);
        System.out.println(aShort/2);
        System.out.println(c);
        System.out.println(f);
        System.out.println(d);
        System.out.println(oasis);
        System.out.println(name);
    }
    
}
